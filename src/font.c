/*
 * font.c — Hershey Simplex vector font + renderer for vekterm.
 *
 * Glyph data: the public-domain Hershey Simplex font digitised by Dr. A. V.
 * Hershey (US National Bureau of Standards), via Paul Bourke's tabulation
 * (http://paulbourke.net/dataformats/hershey/). Each glyph is a list of (x, y)
 * stroke points with baseline at y=0 and +y up; a (-1, -1) point lifts the pen
 * (move, don't draw) before the next point. `width` is the advance to the next
 * glyph. A handful of wide glyphs (& @ { }) exceed the 31-point budget and are
 * left blank — vekterm's splash never uses them.
 *
 * Renderer: vt_draw_string scales each point into the Vectrex integrator range
 * and draws every stroke with v_directDraw32 — the same per-segment path the
 * received frames use, where each segment gets its own SET_OPTIMAL_SCALE. That
 * is what keeps strokes straight; see font.h.
 */
#include "font.h"

/* v_directDraw32 lives in the vendored libpitrex (vectrexInterface.c). Declared
 * locally so this file needs neither the VIA register macros (baremetal) nor the
 * full interface header (the off-target emulator declares it the same way). */
void v_directDraw32(int32_t xStart, int32_t yStart, int32_t xEnd, int32_t yEnd, uint8_t brightness);

/* Integrator units per Hershey unit, per `size` step. 8 puts a size-8 capital
 * at ~21*8*8 ~= 1344 units tall, matching the old BIOS title height. Override
 * with -DVT_FONT_SCALE to recalibrate without editing source. */
#ifndef VT_FONT_SCALE
#define VT_FONT_SCALE 8
#endif

/* 8-bit screen coord -> integrator input coord (v_printString used the same
 * x*128 mapping, so splash placements carry over unchanged). */
#define VT_FONT_POS 128

